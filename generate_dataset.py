def fill_template(template: str, slots: dict) -> str:
    """Fill all placeholders in a template with random slot values."""
    result = template
    for key, values in slots.items():
        placeholder = "{" + key + "}"
        while placeholder in result:
            result = result.replace(placeholder, random.choice(values), 1)
    return result


def generate_queries(templates, slots, extra=None, count=0):
    """Generate queries from templates + slots, plus extra literal queries."""
    seen = set()

    # Split templates into literal (no slots) and slotted
    literal_templates = [t for t in templates if "{" not in t]
    slotted_templates = [t for t in templates if "{" in t]

    # 1. Add all literal templates
    for t in literal_templates:
        seen.add(t)

    # 2. Add extra queries: literal ones directly, slotted ones to slotted pool
    if extra:
        for q in extra:
            if "{" not in q:
                seen.add(q)
            else:
                slotted_templates.append(q)

    # 3. Systematic: for each slotted template, generate all unique fillings
    #    by iterating over the largest slot's values
    if slotted_templates and len(seen) < count:
        # Find which slots each template uses
        for template in slotted_templates:
            if len(seen) >= count:
                break
            used_slots = [k for k in slots if "{" + k + "}" in template]
            if not used_slots:
                continue
            # Use the slot with the most values for systematic iteration
            largest_slot = max(used_slots, key=lambda k: len(slots[k]))
            for val in slots[largest_slot]:
                if len(seen) >= count:
                    break
                filled = template
                # Replace the largest slot with this specific value
                filled = filled.replace("{" + largest_slot + "}", val, 1)
                # Randomly fill remaining slots
                for k in used_slots:
                    if k != largest_slot:
                        while "{" + k + "}" in filled:
                            filled = filled.replace("{" + k + "}", random.choice(slots[k]), 1)
                if "{" not in filled:
                    seen.add(filled)

    # 4. If still short, pad with synthetic queries from combined templates
    if len(seen) < count and slotted_templates:
        pad_attempts = 0
        while len(seen) < count and pad_attempts < count * 30:
            # Pick two different templates and blend them
            t1 = random.choice(slotted_templates)
            t2 = random.choice(slotted_templates)
            # Fill them
            f1 = fill_template(t1, slots)
            f2 = fill_template(t2, slots)
            if "{" not in f1 and "{" not in f2:
                # Create variations by combining or slightly modifying
                variants = [
                    f1,
                    f1.rstrip("?") + " specifically?",
                    "Quick question: " + f1[0].lower() + f1[1:],
                    "Just wondering, " + f1[0].lower() + f1[1:],
                    "Can you clarify: " + f1[0].lower() + f1[1:],
                ]
                for v in variants:
                    if len(seen) < count:
                        seen.add(v)
            pad_attempts += 1

    return list(seen)[:count]


def cleanup_query(query: str) -> str:
    """Fix common grammar artifacts in generated queries."""
    q = query
    # Fix repeated words
    import re
    q = re.sub(r'\b(the|The|a|an|A|An)\s+\1\b', r'\1', q)
    # Fix "Your the X" -> "Your X"
    q = re.sub(r'\b([Yy]our)\s+the\s+', r'\1 ', q)
    # Fix a/an before vowel sounds
    q = re.sub(r'\ba\s+([aeiou]|SLA|urgent|escalation|un(?:authorized|acceptable|resolved))', r'an \1', q, flags=re.IGNORECASE)
    q = re.sub(r'\bA\s+([aeiou]|SLA|Urgent|Escalation|Un(?:authorized|acceptable|resolved))', r'An \1', q)
    # Remove padding artifact prefixes
    for prefix in ["Quick question: ", "Just wondering, ", "Can you clarify: "]:
        if q.startswith(prefix):
            q = q[len(prefix):]
            q = q[0].upper() + q[1:] if q else q
    # Remove padding artifact suffixes
    for suffix in [" specifically?", " specifically"]:
        if q.endswith(suffix):
            q = q[:-len(suffix)]
    # Remove trailing stray punctuation artifacts
    q = q.rstrip(".").strip()
    if q and q[-1] not in ".!?":
        q += "."
    return q


